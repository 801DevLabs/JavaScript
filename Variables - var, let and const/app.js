console.log(greeting) // LOG A VARIABLE
console.log([1,2,3,4]) // LOG AN ARRAY
console.log({a:1, b:2}) // LOG AN OBJECT
console.table({a:1, b:2}) // LOG AN OBJECT AS A TABLE

console.error('This is some error') // LOG AN ERROR
console.clear(); // CLEAR THE CONSOLE
console.warn('This is a warning'); // LOG A WARNING
